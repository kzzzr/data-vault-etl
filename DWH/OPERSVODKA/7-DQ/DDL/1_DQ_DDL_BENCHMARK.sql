-- создаем таблицу бенчмарков проверок
CREATE TABLE IF NOT EXISTS ODS_OPERSVODKA.DDS_DQ_BENCHMARK(
PLANT_PRODUCT_ID 	VARCHAR(64), 
NAME				VARCHAR(64), 
BTYPE				VARCHAR(64), 
VALUE 				NUMERIC(18,5), 
PERIOD_FROM 		DATE, 
PERIOD_TO 			DATE,
LOAD_TS 			TIMESTAMP, 
PRIMARY KEY			(PLANT_PRODUCT_ID, NAME, BTYPE))
ORDER BY 			PLANT_PRODUCT_ID, NAME, BTYPE UNSEGMENTED ALL NODES;